local function createLevel( map )
	local blocks = {}
	for i, v in pairs( map ) do
		block = { index = i, x = x, y = y }
		if v == 0 or v > 19 then
			block.isPath = true
			if x > -size and x < 28 * size then background:add( tiles[ 15 ], x, y ) end
			if x > 192 and x < 640 and y > 256 and y < 640 then
			else
				if i == 93 or i == 118 or i == 813 or i == 838 then
					block.bid = bonus:add( book, x, y )
					block.hasPower = true
				else
					block.bid = bonus:add( potion, x, y )
					block.hasBonus = true
				end
			end
		elseif v < 18 then
			block.isWall = true
			if x > -size and x < 28 * size then background:add( tiles[ v ], x, y ) end
		end
		table.insert( blocks, block )
		 x = x + size
		if i % 30 == 0 then
			x = -size
			y = y + size
		end
	end
	return blocks
end
